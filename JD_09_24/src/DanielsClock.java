
public class DanielsClock {
	// Initialize Variables
	private int hrs;
	private int min;
	private int sec;
	
	// Constructor Method
	public DanielsClock(){
		hrs = 0;
		min = 0;
		sec = 0;
	}
	
	public DanielsClock(int call_hrs, int call_min, int call_sec){
		hrs = call_hrs;
		min = call_min;
		sec = call_sec;
	}

}
